 public void obtenerDatos (){
        List<Destinos> Destinos= new DAODestinos().obtenerDatos();
        DefaultTableModel modelo= new DefaultTableModel();
        
        String [] columnas={"id_Destinos","NombreDestino", "ApellidoDestino",
            "Descripcion"};
        
        modelo.setColumnIdentifiers(columnas);
        for (Destinos au:Destinos){
            
            String[]renglon={Integer.toString(au.getIdDestinos()),au.getNombresDestinos(),
                au.getUbicacionDestinos(),au.getDescripccionDestinos(),
                String.valueOf(au.equals(au))};
            modelo.addRow(renglon);
             
            }
       jTableDestino.setModel(modelo);
    }
    
    
    
    
