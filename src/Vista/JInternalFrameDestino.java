    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        // TODO add your handling code here:
        String nomb=jTextNombreDestino.getText();
        String ubi=jTextUbicacionDestino.getText();
        String desc= jTextDescripcion.getText();
     
         try {
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(rootPane, "La edad debe ser un número válido");
        return; // Salir de la función si la conversión falla
    }
        if(nomb.contentEquals("")||ubi.contentEquals("")||
                desc.contentEquals(""))
                {
            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
            
        }else{
            try{ 
   
                Destinos au=new DAODestinos().Insertar(nomb, ubi, desc );
                JOptionPane.showMessageDialog(rootPane, "Registro agregado");
                
            }catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
                
            }
        }
        obtenerDatos();
        limpiarCampos();
    }//GEN-LAST:event_jButtonAgregarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        int fila=this.jTableDestino.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro dde la tabla" );
                
        }
        else{
            try{
                int id=Integer.parseInt((String)this.jTableDestino.getValueAt(fila, 0).toString());
                String nomb=(String)this.jTableDestino.getValueAt(fila, 1);
                String ubi=(String)this.jTableDestino.getValueAt(fila, 2);
                String desc=(String)this.jTableDestino.getValueAt(fila, 3);
                
                jTextIDdestino.setText(""+id);
                jTextNombreDestino.setText(""+nomb);
                jTextUbicacionDestino.setText(""+ubi);
                jTextDescripcion.setText(""+desc);
                }catch(NumberFormatException e){
                        e.printStackTrace();
                        }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

