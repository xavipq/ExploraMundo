 public void actualizarDestino(){
     int id=Integer.parseInt(this.jTextIDdestino.getText());
     String nomb=this.jTextNombreDestino.getText();
     String ubi=this.jTextUbicacionDestino.getText();
     String desc=this.jTextDescripcion.getText();
     
     DAODestinos dao=new DAODestinos();
     int res=dao.Actualizar(id, nomb, ubi, desc );
     if (res==1){
         JOptionPane.showMessageDialog(rootPane, "Autor Actualizado!");
         
     }
     else{
         JOptionPane.showMessageDialog(rootPane, "Ocurrió un error!!");
     }
 }

