   public void obtenerDatos (){
         List<Paquetes> paquetes= new DAOPaquetes().obtenerDatos();
        DefaultTableModel modelo= new DefaultTableModel();
        
        String [] columnas={"id_paquete","Nombrepaquete", "Descripcionpaquete",
            "IDdestino"};
        
         modelo.setColumnIdentifiers(columnas);
        for (Paquetes au:paquetes){
            
            String [] renglon={Integer.toString(au.getIDpaquetes()),au. getNombrespaquetes(),
                au.getDescripcionpaquete(),
                au.getIDdestino()+
                 String.valueOf(au.equals(au))};
            modelo.addRow(renglon);
                
          
        }
         jTablePaquetes.setModel(modelo);
   }
