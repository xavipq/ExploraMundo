    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
         int fila=this.jTablePaquetes.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro dde la tabla" );
                
        }
         else{
            try{
                int id=Integer.parseInt((String)this.jTablePaquetes.getValueAt(fila, 0).toString());
                String nomb=(String)this.jTablePaquetes.getValueAt(fila, 1);
                String ubi=(String)this.jTablePaquetes.getValueAt(fila, 2);
                String desc=(String)this.jTablePaquetes.getValueAt(fila, 3);
                
                jTextIDpaquete.setText(""+id);
                jTextNombrePaquete.setText(""+nomb);
                jTextDescripcionPaquete.setText(""+ubi);
                jTextIDdestino.setText(""+desc);
                }catch(NumberFormatException e){
                        e.printStackTrace();
                        }
        }
        
    }//GEN-LAST:event_jButtonEditarActionPerformed

